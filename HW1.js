console.log(null + 3)   // null преобразуется в ноль и происходит обычная математическая операция 0+3

console.log(5 && "qwerty") // оператор "And" вернет трушное значение второго операнда , если оба true. В данном случае "5" и "qwerty"- true, потому на выходе будет qwerty

console.log(+'40' + +'2' + "hillel"); // Конкатанация, инты переводятся в строку , на выходе объедененная строка

console.log('10' - 5 === 6); //сравниваем значения, строка 10 переводится в инт, отнимается 5 и сравнивается с 6, на выходе - false , тк 5!=6

console.log(true + false) // boolean переводится в инт ( 1 и 0 ), 1+0=1

console.log('4px' - 3) // в случае с отниманием строка,которая содержит в себе не только число не переведется в число , на выходе NaN

console.log('4' - 3) // строка содержит в себе только числа , происходит преобразование в инт и обычная арифметическая операция , на выходе 1

console.log('6' + 3 ** 0); // операция в добавление и возведение в степень , сначала возводится в степень , и далее происходит конкатанация 6 и 1 , в итоге 61

console.log(12 / '6') // 6 преобразуетс в инт и происходит арифметическая операция , на выходе 2

console.log('10' + (5 === 6)); // конкатанация стринги "10" и false ( тк 5!=6, false переведется в строку)

console.log(null == ''); // Null не ровняется пустой строке , на выходе false 

console.log(3 ** (9 / 3)); //сначала выполняется операция в скобках , обычное деление , получаем 3 и поле этого возведение 3 в 3 степень, на выходе 27

console.log(!!'false' == !!'true'); // Две не пустые строки сравниваются, оба будут иметь значение true , на выходе - true 

console.log(0 || '0' && 1);  //'0' && 1: оператор && оценивает оба операнда и возвращает последнее истинное значение или первое ложное значение. В данном случае «0» — истинное значение, поэтому результат равен 1.0 || '0' && 1: || оператор возвращает первое истинное значение или последнее ложное значение. В этом случае 0 является ложным, поэтому происходит переход к следующему операнду.(0 || '0' && 1) эквивалентно (0 || 1). || Оператор возвращает первое истинное значение, равное 1.

console.log((+null == false) < 1);  // +null - интерпретируется как 0 или false, сравнивается с false , получаем true  , далее сравниваем 1 < 1  - это false на выходе 

console.log(false && true || true) // false && true: оператор && возвращает первое ложное значение, если оба операнда истинны; в противном случае он возвращает последнее истинное значение. В этом случае, поскольку первый операнд false является ложным, результат будет ложным.Оператор (логическое ИЛИ) возвращает первое истинное значение, если любой операнд является правдивым; в противном случае возвращается последнее ложное значение. В этом случае первый операнд false является ложным, поэтому результатом будет истинный второй операнд.На выходе True

console.log(false && (false || true)); // выполняем оператор в скобках получаем true , далее оператор И , значения разные, потому получаем false на выходе 

